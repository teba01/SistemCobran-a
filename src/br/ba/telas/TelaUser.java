     public void readTable() { 
        DefaultTableModel modelo = (DefaultTableModel) TabelaU.getModel();
        modelo.setNumRows(0);
        UsuarioDAO udao = new UsuarioDAO();
        
        for (Usuario u : udao.read()) {
            modelo.addRow(new Object[]{
                u.getNome(),
                u.getCargo(),
                u.getEmail(),
                u.getLogin(),
                u.getSenha()
            });
        }
    }
    public void readJTableForName(String nome) {
        
        DefaultTableModel modelo = (DefaultTableModel) TabelaU.getModel();
        modelo.setNumRows(0);
        UsuarioDAO udao = new UsuarioDAO();

        for (Usuario u : udao.readForName(nome)) {
           modelo.addRow(new Object[]{
                u.getNome(),
                u.getCargo(),
                u.getEmail(),
                u.getLogin(),
                u.getSenha()
            });
        }
    }
    
